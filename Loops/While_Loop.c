#include<stdio.h>
int main()
{ 
  //Declaring the variable
   
    int i;
  
  //Assign the value to the variable i
   
    i = 1;

   /*Now we have to Print the Bhavani name repeted times
    * Now use the loop concept
    * So here we are using While loop*/

    while(i<10)
    {
       printf("Bhavani\n");
       
       i++;

    }
    
    //Exit from the main funcation.

    return 0;    
}
